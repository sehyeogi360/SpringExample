package com.marondal.spring.ex.lifecycle.model;

public class Person {
	//우클릭 -> source -> getter/setter
	private String name;
	private int age;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
}
